//ChooseList Start
var list = document.querySelector('#choose-list');
list.addEventListener('click', function (item) {
    if (item.target.tagName == 'LI') {
        document.querySelector('.mb-1.buton').textContent = item.target.textContent;
    }
});//ChooseList End

//ChooseWeb Start
var langList = document.querySelector('#choose-lang');
var webList = document.querySelector('#choose-web');
webList.addEventListener('click', function (web) {
    if (web.target.tagName == 'LI') {
        document.querySelector('#webSite').textContent = web.target.textContent;
    }

    if (web.target.textContent == "GOOGLE TRANSLATE") {
        document.querySelector('#yourLang').textContent = "CHOOSE YOUR LANG";
        langList.innerHTML = " ";
        document.querySelector('#webSite').value = "google";
        langGoogle.forEach(function (google) {
            var li = '';
            li = '<li class="dropdown-item" value="' + google.code + '">' + google.name + '</li>';
            langList.innerHTML += li;
        });
    }
    else {
        document.querySelector('#yourLang').textContent = "CHOOSE YOUR LANG";
        langList.innerHTML = " ";
        document.querySelector('#webSite').value = "yandex";
        langYandex.forEach(function (yandex) {
            var li = '';
            li = '<li class="dropdown-item" value="' + yandex.code + '">' + yandex.name + '</li>';
            langList.innerHTML += li;
        });
    }
});//ChooseWeb End

//ChooseLang Start
langList.addEventListener('click', function (lang) {
    if (lang.target.tagName == 'LI') {
        document.querySelector('#yourLang').textContent = lang.target.textContent;

        if (document.querySelector('#yourLang').textContent == "GOOGLE TRANSLATE") {
            langGoogle.forEach(function (google) {
                if (lang.target.textContent == google.name) {
                    document.getElementById('yourLang').value = google.code;
                }
            });
        }
        else {
            langYandex.forEach(function (yandex) {
                if (lang.target.textContent == yandex.name) {
                    document.getElementById('yourLang').value = yandex.code;
                }
            });
        }

    }
});//ChooseLang End

//Translate Button Start
function trans() {
    var kod = document.getElementById('webSite').value;
    console.log(kod);
    if (kod == "google") {
        var li = "https://translate.google.com/?source=gtx#view=home&op=translate&sl=en&tl=" + document.getElementById('yourLang').value + "&text=" + document.getElementById('word').textContent;
        window.open(li, "_blank");
    }
    else if (kod == "yandex") {
        var li = "https://translate.yandex.com/?lang=en-" + document.getElementById('yourLang').value + "&text=" + document.getElementById('word').textContent;
        window.open(li, "_blank");
    }
    else {
        return true;
    }
}//Translate Button End

